<h2 align="center">Posting Policy for Oakland University</h2>
<strong>SUBJECT:</strong> 	      			UNIVERSITY POSTING POLICY </br></br>
<strong>NUMBER:</strong> 	       			486</br></br>
<strong>AUTHORIZING BODY:</strong>      	PRESIDENT'S CABINET</br></br>
<strong>RESPONSIBLE OFFICE:</strong> 		CENTER FOR STUDENT ACTIVITIES</br></br>
<strong>DATE ISSUED:</strong> 				APRIL 1987</br></br>
<strong>LAST UPDATE:</strong> 				JANUARY 2002</br></br>

<strong><ins>RATIONALE:</ins></strong></br></br>

<strong><ins>POLICY:</ins></strong></br></br>

Posting is defined as the hanging of information, advertising, etc. on any campus bulletin board, window, door, wall or other University property.  Oakland University reserves the right to refuse permission to any individual or group for posting that:</br>

        <p>a.  Would seriously impede normal University activity,</br>

        b.  Does not follow Oakland University guidelines and procedures set forth in this policy,</br>

        c.  Would make posting particular material inappropriate due to special circumstances, and</br>

        d.  Contains contents which are in violation of University policies or ordinances, state or federal law.</p></br>

<strong>I.  Approval for Posting on Campus</strong></br>

        <p>A. Recognized Oakland University Organizations</br>

            1.  Any officially recognized Oakland University department or organization may post materials which pertain to the activities of the group
                 on any authorized bulletin board or display area, subject to restrictions as defined in this policy.</br>

        B. Off-Campus Groups or Individuals</br>

            1.  Any off-campus group or individuals wishing to post any type of materials on campus must obtain prior approval from Center for Student
                 Activities. If approved, this material may be posted on "Non-CSA Posting Area" boards.</br>

            2.  When advertising for a service, activity or event which is already provided for by a University department or organization or when
                 materials are not consistent with the University role and mission, a disclaimer must appear on each piece of posted material.  This
                 disclaimer must state that it is not a "University sponsored" service, activity or event.</br></p>

<strong>II.  Posting Sites</strong></br>
 
<p>Generally only bulletin boards are available for use to post information to the campus community.  Groups should ask about the appropriate use of
bulletin boards prior to use.  "CSA Posting Areas" are reserved for providing information about University activities.  Postings during the annual
student government elections are subject to Oakland University Student Congress Election Commission elections guidelines.</br></p>
 
<strong>III.  Posting Regulations</strong></br>

        <p>1. Posters, flyers and other printed material are not to be affixed to restroom mirrors, plants, trees, poles and painted surfaces in and any
            OU building, including the Oakland Center and the residence halls.</br>

        2. Only thumbtacks, not staples or tape, are to be used to post printed information.</br>

        3. Printed material posted will be removed monthly.</br>

        4. Any undated material will be removed.</br>

        5. Personal posting or advertisements are not to be posted on bulletin boards designated as "CSA Posting Areas."</br>

        6. Only one posting of printed material can be posted on bulletin boards.</br>

        7. Postings must be within the frame of the bulletin board. Do not post on top of other printed material.  Do not remove other printed material
            already posted unless outdated. </br>

        8. There is no posting of adhesive-backed materials (i.e., stickers) on any surfaces, including trees and poles.</br>

        9. Banners can be ordered and scheduled to be hung in the Oakland Center through the Center for Student Activities Office.</br>

       10. Banners or other printed materials to be hung or posted in and around the residence halls must be approved through the Housing Office.</br>

       11. Placement of table tents in the Pioneer Food Court can be scheduled through the Center for Student Activities Office, and Vandenberg Dining
             Center table tents through the Housing Office. </br>

       12. Posting by "chalking" can be done on sidewalks where there is no overhang and chalk can be washed away by inclement weather. Chalking
             cannot be done on any surface except sidewalks and the bridge. Paint of any kind is prohibited, including chalk paint. </br>

       13. Paint can be used only to decorate the "spirit rocks" located outside of the Oakland Center (north end) and in the Hamlin Circle.</br>

       14. There is no posting on vehicles in any University parking lot.</br>

       15. Student and Greek organization mailboxes in the Center for Student Activities Office can be used for distributing information about on-or
             off-campus activities, products and services with approval from the CSA staff.</br>

       16. Mailboxes in the residence halls are not available for distributing information about on-or off-campus activities sponsored by student and
             Greek organizations.</br></p>

<p>Violating the posting policy can result in disciplinary action and/or service charges for individuals and groups found responsible.  Violators may also
be charged with violation of OU ordinance #4.21.</br></p>
 
<strong>IV.  Disclaimer</strong></br>

<p>By its review and acceptance of materials for posting on campus, Oakland University does not endorse or warrant the products and services advertised on
these printed materials.</br></p>
 
<strong>V.  Enforcement</strong></br></br>

Dean of Students Office</br>
Center for Student Activities</br>
 
<p>Additional information about the posting policy or complaints regarding violations of this policy can be directed to the Dean of Students Office or the
Center for Student Activities. </br></p>
